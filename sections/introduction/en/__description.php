<h3>Unzipping Template Package</h3>
<p>After unzipping the template package you will find 5 folders: &quot;<strong>documentation</strong>&quot;,
    &quot;<strong>screenshots</strong>&quot;, &quot;<strong>responsive</strong>&quot;, &quot;<strong>mailchimp</strong>&quot;,
    &quot;<strong>campaingmonitor</strong>&quot;. You can also see a zip archive called &quot;<strong>sources_############.zip</strong>&quot;
    that you need to unzip. </p>
<p>The sources package contains all source files for the template. The package is password protected and can be
    extracted only with <a href="/help/unzipping-your-template.html" target="_blank">WinZip (Windows OS)</a> and <a
        href="/help/how-unzip-template-mac.html" target="_blank">StuffitExpander (MAC OS)</a> software.</p>
<p>You can download both applications for free using the free trial options:</p>
<ul class="marked-list">
    <li>
        <a href="http://www.winzip.com/downauto.cgi?o=1&amp;file=http://download.winzip.com/winzip160.exe&amp;email=&amp;er=&amp;os=win"
           target="_blank">WinZip</a></li>
    <li><a href="http://www.stuffit.com/mac-stuffit-download.html" target="_blank">StuffitExpander</a></li>
</ul>
<p class="alert alert-warning">While unzipping "sources_############.zip" archive you will be prompted to enter a password,
    which you can find at your Product Download Page (open the link in the email you received from our company).</p>
<p>As soon as you are done with the unzipping the template you should have 6 folders in total: &quot;<strong>documentation</strong>&quot;,
    &quot;<strong>screenshots</strong>&quot;, &quot;<strong>responsive</strong>&quot;, &quot;<strong>mailchimp</strong>&quot;,
    &quot;<strong>campaingmonitor</strong>&quot;, &quot;<strong>sources</strong>&quot;.</p>

