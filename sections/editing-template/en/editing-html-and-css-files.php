<h3>Editing HTML</h3>
<p>All editable HTML files are located in folders of the template package (index.html).

    To be able to edit the file, you'll need some knowledge of these languages: HTML, CSS. You will also need to use an
    editor. <b>Notepad</b> (for Windows) is the simplest kind of editor that will open all the above file types.
    However, it's not convenient for editing, so you'd better get one of the following editors: </p>
<ul class="marked-list">
    <li><b>SublimeText</b> <a href="http://www.sublimetext.com/">http://www.sublimetext.com/</a></li>
    <li><b>Dreamweaver</b> <a href="https://creative.adobe.com/products/dreamweaver">https://creative.adobe.com/products/dreamweaver</a>
    </li>
    <li><b>Notepad++</b> <a href="http://notepad-plus-plus.org/">http://notepad-plus-plus.org/</a></li>
</ul>