<h3>Editing PSD files</h3>
<p>PSD files are located in the &quot;<strong>sources/psd</strong>&quot; folder in your template package. PSD are the
    source files of the template design. They are divided into layers so any part of the template design can be changed.
</p>
<p>During the customization process you can refer to the PSD files in order to change the images used in the content
    pages or in the design. </p>
